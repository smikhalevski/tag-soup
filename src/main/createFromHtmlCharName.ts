function decodeEntities(entities: Record<string, any>) {
  for (const key of Object.keys(entities)) {
    const b = entities[key];
    entities[key] = b < 65535 ? String.fromCharCode(b) : String.fromCharCode(b % 65535, (b - b % 65535) / 65535);
  }
  return entities;
}
